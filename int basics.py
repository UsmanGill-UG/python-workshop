# Variable naming
# Variable name can't start with a number
# 1a = 10 # This will give us error

# We can't add space between the variable
# my name = 10 # This will give us error

# int
a = 10 
b = 10

sum = a + b
print(sum) # 20

minus = a - b
print(minus) # 0

multiply = a * b
print(multiply) # 100

divide = a / b
print(divide) # 1.0

# We can also directly use this in print()
# But it will not be stored in any variable so we can't use it later
print(a + b) # 20
print(a - b) # 0
print(a * b) # 100
